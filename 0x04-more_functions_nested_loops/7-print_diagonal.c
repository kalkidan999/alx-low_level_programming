#include "holberton.h"
/**
* print_diagonal - check the code for Holberton School students.
* @n: integer
* Return: Always 0.
*/
void print_diagonal(int n)
{
int i;
int j;
j = n;
if(j < 0)
_putchar('\n');
for (n = 0; n > 0; n--)
{
for(i = 0; i <= 0; i++)
{
_putchar(' ');
_putchar('\\');
_putchar('\n');
}
}
return;
}        
